<?php

namespace App\Support;

/**
 * Пределы карты — общие для всего, что её валидирует или адресует её клетки.
 * Раньше каждый контроллер писал своё число, и позиция игрока на карте шире 256
 * молча переставала сохраняться. Клиентская калька — `MAX_MAP_SIZE` в
 * `resources/js/game/map.ts`.
 */
class MapLimits
{
    /** Сторона карты в тайлах: 512×512 = 262 тыс. клеток, выше уже неадекватно по памяти. */
    public const MAX_SIZE = 512;

    /** Наибольшая координата клетки — клетки нумеруются с нуля. */
    public const MAX_COORD = self::MAX_SIZE - 1;
}
