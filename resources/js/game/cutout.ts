// Овал прозрачности, которым персонаж «просвечивает» сквозь то, что рисуется
// поверх него: верхушки стен и высокие части предметов.

export const CUTOUT_RX = 34;
export const CUTOUT_RY = 44;
export const GHOST_ALPHA = 0.32;

// Кадр LPC-персонажа — 64 px, ноги на FEET_Y ниже центра тайла, значит
// макушка ровно на 48 px выше центра. Это же граница тайловой строки:
// центр тайла = row*32 + 16, макушка = (row - 1) * 32.
export const SPRITE_TOP = 48;

// точек в полигоне овала — на глаз граница неотличима от эллипса
const STEPS = 48;

/**
 * Полигон выреза: овал, срезанный сверху по линии `topY`.
 *
 * Срез — не украшение, а правило видимости. Всё, что рисуется поверх игроков,
 * стоит на какой-то строке карты: верхушка стены — на строке стены под ней,
 * высокая часть предмета — на строке его основания. Просвечивать нужно только
 * то, что стоит НА уровне персонажа или ниже, то есть перекрывает его; всё,
 * что стоит выше по карте, находится «за спиной», и дырка в нём показывала бы
 * изнанку сцены.
 *
 * Проверять это по каждому объекту не нужно: любой overhead-элемент, стоящий
 * выше персонажа, целиком лежит выше линии его макушки (его спрайт занимает
 * строки НАД собственным основанием, а основание уже выше персонажа). Поэтому
 * достаточно не пускать вырез выше макушки — и «задние» объекты не тронуты.
 */
export function cutoutPolygon(cx: number, cy: number, topY: number, rx = CUTOUT_RX, ry = CUTOUT_RY): number[] {
    const points: number[] = [];
    for (let i = 0; i < STEPS; i++) {
        const angle = (i / STEPS) * Math.PI * 2;
        points.push(cx + Math.cos(angle) * rx, Math.max(cy + Math.sin(angle) * ry, topY));
    }
    return points;
}
