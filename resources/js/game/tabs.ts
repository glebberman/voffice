/**
 * Одна вкладка на пользователя.
 *
 * Presence-канал считает участников по id пользователя, поэтому две вкладки
 * одного человека — это два независимых отправителя с одинаковым id. Каждая шлёт
 * свой heartbeat, и остальные клиенты видят персонажа прыгающим между двумя
 * позициями: свои whisper-ы вкладка игнорирует, а чужие — нет.
 *
 * Решение: управление держит та вкладка, которую открыли последней. Новая при
 * входе здоровается, остальные вкладки этого же пользователя замолкают и
 * показывают предложение вернуть управление себе.
 */

/** Идентификатор вкладки: живёт ровно столько, сколько открыта страница. */
export function newTabId(): string {
    return crypto.randomUUID();
}

export interface TabHello {
    id: number; // пользователь
    tab: string;
    at: number; // когда вкладка поздоровалась (Date.now той же машины)
}

/**
 * Уступить ли управление пришедшему приветствию: управление держит вкладка,
 * поздоровавшаяся последней.
 *
 * Решение принимается по данным из самого приветствия, а не по тому, кто что
 * успел услышать. Это важно: подписка на канал и своё «привет» происходят
 * одновременно, поэтому вкладка, открытая позже, приветствие первой вообще не
 * получит. Сравнивая метки, обе стороны приходят к одному ответу, и активной
 * остаётся ровно одна — даже если разговор был односторонним.
 *
 * Часы у вкладок общие (один браузер), поэтому метки сравнимы напрямую. Ничья
 * (одна миллисекунда) разводится идентификаторами — тоже симметрично.
 */
export function shouldYieldTo(hello: TabHello, selfId: number, myTab: string, myHelloAt: number): boolean {
    if (hello.id !== selfId || hello.tab === myTab) {
        return false;
    }
    if (hello.at !== myHelloAt) {
        return hello.at > myHelloAt;
    }

    return hello.tab > myTab;
}
